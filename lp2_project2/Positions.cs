namespace lp2_project2
{
    /// <summary>
    /// this struct allows us to set gameobjects' positions with an y and x
    /// coordinates
    /// </summary>
    public struct Positions
    {
        public int X;
        public int Y;

        public Positions(int x, int y)
        {
            X = x;
            Y = y;
        }
    }
}
